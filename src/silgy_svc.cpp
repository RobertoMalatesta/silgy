#include <silgy.h>


/* ======================================================================= */
/* =============================== SERVICES ============================== */
/* ======================================================================= */


/* --------------------------------------------------------------------------
   Service
-------------------------------------------------------------------------- */
void getCustomer()
{
    OUT_HTML_HEADER;
    OUT("<h1>%s</h1>", APP_WEBSITE);
    OUT("<p>Hello from getCustomer service!</p>");
    OUT_HTML_FOOTER;
}


/* --------------------------------------------------------------------------
   Service
-------------------------------------------------------------------------- */
void getAccounts()
{
    OUT_HTML_HEADER;
    OUT("<h1>%s</h1>", APP_WEBSITE);
    OUT("<p>Hello from getAccounts service!</p>");
    OUT_HTML_FOOTER;
}


/* ======================================================================= */
/* ========================== END OF SERVICES ============================ */
/* ======================================================================= */



/* --------------------------------------------------------------------------
   Entry point
-------------------------------------------------------------------------- */
void silgy_svc_main()
{
    if ( SVC("getCustomer") )
        getCustomer();
    else if ( SVC("getAccounts") )
        getAccounts();
}


/* --------------------------------------------------------------------------
   Server start
   Return true if successful
-------------------------------------------------------------------------- */
bool silgy_svc_init()
{
    return true;
}


/* --------------------------------------------------------------------------
   Server stop
-------------------------------------------------------------------------- */
void silgy_svc_done()
{
}
